%pp(-0.0004,3) returns '-4.000 \times 10^{-4}'
function s=pp(x,n)
    exponent=floor(log10(abs(x)));
    mantissa=x/(10^exponent);
    s=sprintf('%*.*f \\times 10^{%d}',n+3,n,mantissa,exponent);
end