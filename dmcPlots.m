        ccleg(k) = "-1";
    else
        ccleg(k) = sprintf("%.3f",(cc(k)));
    end
    f(k) = data{k}(1,ind("f"));
    fleg(k) = string(f(k));
    dt(k) = data{k}(1,ind("dt"));
    dtleg(k) = pp(dt(k),1);
    if C8(k) == -1
        x0 = 1;
        B = bb(k); C = cc(k);
        fun = @(R)(-C4*(R^2-C^2)/(R^2+C^2)*1/(B^2+R^2)^2);
        [x,fval] = fminsearch(fun,x0);
        De(k) = -fval;
        Re(k) = x;
    else
        De(k) = 82.0563^2/(4*data{k}(1,ind("C8")));
        Re(k) = (2*data{k}(1,ind("C8"))/82.0563)^(1/4);
    end
    Deleg(k) = pp(De(k),3);
    if length(data{k}) >= 19
        N1(k) = data{k}(19,ind("N1"));
    end
    N1leg(k) = string(N1(k));
end
